type vec struct{ x, y, z int }

func (v vec) add(w vec) vec {
	return vec{
		v.x + w.x,
		v.y + w.y,
		v.z + w.z,
	}
}

type particle struct {
	p, v, a vec
}

func (p particle) dist() int {
	return utils.Abs(p.p.x) + utils.Abs(p.p.y) + utils.Abs(p.p.z)
}

func (p particle) step() particle {
	p.v = p.v.add(p.a)
	p.p = p.p.add(p.v)
	return p
}

func parse(s string) []particle {
	var ps []particle
	for _, line := range utils.Lines(s) {
		var p particle
		utils.Sscanf(line, `p=<%d,%d,%d>, v=<%d,%d,%d>, a=<%d,%d,%d>`,
			&p.p.x, &p.p.y, &p.p.z, &p.v.x, &p.v.y, &p.v.z, &p.a.x, &p.a.y, &p.a.z)
		ps = append(ps, p)
	}
	return ps
}

func main() {
	// part 1
	ps := parse(input)

	// step all particles forward 10000 steps, then find closest
	after10k := make([]particle, len(ps))
	for i, p := range ps {
		for j := 0; j < 10000; j++ {
			p = p.step()
		}
		after10k[i] = p
	}
	utils.Println(utils.MinimumIndex(len(after10k), func(i int) int {
		return after10k[i].dist()
	}))

	// part 2
	pos := make(map[vec][]particle, len(ps))
	for _, p := range ps {
		pos[p.p] = []particle{p}
	}
	for i := 0; i < 10000; i++ {
		// step all particles forward
		newpos := make(map[vec][]particle, len(pos))
		for _, p := range pos {
			np := p[0].step()
			newpos[np.p] = append(newpos[np.p], np)
		}

		// remove collisions
		for p, ps := range newpos {
			if len(ps) > 1 {
				delete(newpos, p)
			}
		}
		pos = newpos
	}

	utils.Println(len(pos))
}
