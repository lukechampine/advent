type point struct {
	x, y, z int
}

func (p point) dist() int {
	return utils.Abs(p.x) + utils.Abs(p.y) + utils.Abs(p.z)
}

type nanobot struct {
	point
	r int
}

func (n nanobot) seesPoint(p point) bool {
	dx := utils.Abs(n.x - p.x)
	dy := utils.Abs(n.y - p.y)
	dz := utils.Abs(n.z - p.z)
	return (dx + dy + dz) <= n.r
}

func (n nanobot) rangeOverlaps(m nanobot) bool {
	dx := utils.Abs(n.x - m.x)
	dy := utils.Abs(n.y - m.y)
	dz := utils.Abs(n.z - m.z)
	d := dx + dy + dz
	return n.r+m.r >= d
}

