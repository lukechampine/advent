func parse(str string) []bool {
	b := make([]bool, len(str))
	for i := range b {
		b[i] = str[i] == '#'
	}
	return b
}

func neighborCount(lights [][]bool, row, col int) int {
	neighbors := []struct {
		row, col int
	}{
		{row - 1, col - 1},
		{row - 1, col},
		{row - 1, col + 1},
		{row, col - 1},
		// skip self
		{row, col + 1},
		{row + 1, col - 1},
		{row + 1, col},
		{row + 1, col + 1},
	}
	var count int
	for _, n := range neighbors {
		if n.row >= 0 && n.row < len(lights) &&
			n.col >= 0 && n.col < len(lights[n.row]) &&
			lights[n.row][n.col] {
			count++
		}
	}
	return count
}

func step(lights [][]bool) [][]bool {
	next := make([][]bool, len(lights))
	for i := range next {
		next[i] = make([]bool, len(lights[i]))
	}
	for row := range lights {
		for col := range lights[row] {
			lit := lights[row][col]
			count := neighborCount(lights, row, col)
			if lit && (count == 2 || count == 3) {
				next[row][col] = true
			}
			if !lit && count == 3 {
				next[row][col] = true
			}
		}
	}
	return next
}

