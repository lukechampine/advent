type room struct {
	name     string
	sectorID int
	checksum string
}

type runeCount struct {
	r rune
	n int
}

type byCount []runeCount

func (bc byCount) Len() int      { return len(bc) }
func (bc byCount) Swap(i, j int) { bc[i], bc[j] = bc[j], bc[i] }
func (bc byCount) Less(i, j int) bool {
	// first sort by count
	if bc[i].n != bc[j].n {
		return bc[i].n > bc[j].n
	}
	// sort alphabetically
	return bc[i].r < bc[j].r
}

func (r *room) isReal() bool {
	counts := make(map[rune]int)
	for _, c := range r.name {
		if c == '-' {
			continue
		}
		counts[c]++
	}
	var countSlice []runeCount
	for c, n := range counts {
		countSlice = append(countSlice, runeCount{c, n})
	}
	sort.Sort(byCount(countSlice))

	for i, c := range countSlice[:5] {
		if c.r != rune(r.checksum[i]) {
			return false
		}
	}
	return true
}

func (r *room) decrypt() string {
	d := make([]byte, len(r.name))
	for i, c := range r.name {
		if c == '-' {
			d[i] = ' '
		} else {
			d[i] = byte(((int(c-'a') + r.sectorID) % 26) + 'a')
		}
	}
	return string(d)
}

func filter(rs []room) []room {
	filtered := rs[:0]
	for _, r := range rs {
		if r.isReal() {
			filtered = append(filtered, r)
		}
	}
	return filtered
}

func parse(str string) []room {
	var rs []room
	for _, line := range utils.Lines(str) {
		dash := strings.LastIndex(line, "-") + 1
		brak := strings.LastIndex(line, "[") + 1
		rs = append(rs, room{
			name:     line[:dash-1],
			sectorID: utils.Atoi(line[dash : brak-1]),
			checksum: line[brak : brak+5],
		})
	}
	return rs
}

